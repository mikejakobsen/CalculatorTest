module.exports = {
  add: function (num1, num2) {
    return num1 + num2
  },

  subtract: function (num1, num2) {
    return num1 - num2
  },

  multiplication: function (num1, num2) {
    return num1 * num2
  },

  division: function (num1, num2) {
    return num1 / num2
  },

  pow: function (num, power) {
    return Math.pow(num, power)
  }
}
